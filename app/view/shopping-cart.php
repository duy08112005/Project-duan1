<?php
// Bắt đầu session để lưu giỏ hàng
session_start();

// Kiểm tra xem giỏ hàng đã được khởi tạo chưa
if (!isset($_SESSION['cart'])) {
    $_SESSION['cart'] = [];
}

// Thêm sản phẩm vào giỏ hàng
if ($_SERVER['REQUEST_METHOD'] == 'POST' && isset($_POST['add_to_cart'])) {
    $product_id = $_POST['product_id'];
    $product_name = $_POST['product_name'];
    $product_price = $_POST['product_price'];
    $quantity = $_POST['quantity'];

    // Kiểm tra nếu sản phẩm đã có trong giỏ hàng thì tăng số lượng
    $found = false;
    foreach ($_SESSION['cart'] as &$item) {
        if ($item['product_id'] == $product_id) {
            $item['quantity'] += $quantity;
            $found = true;
            break;
        }
    }

    // Nếu chưa có, thêm mới sản phẩm vào giỏ hàng
    if (!$found) {
        $_SESSION['cart'][] = [
            'product_id' => $product_id,
            'product_name' => $product_name,
            'product_price' => $product_price,
            'quantity' => $quantity
        ];
    }
}

// Xóa sản phẩm khỏi giỏ hàng
if ($_SERVER['REQUEST_METHOD'] == 'POST' && isset($_POST['remove_item'])) {
    $product_id = $_POST['product_id'];

    // Duyệt qua giỏ hàng và xóa sản phẩm có ID tương ứng
    foreach ($_SESSION['cart'] as $key => $item) {
        if ($item['product_id'] == $product_id) {
            unset($_SESSION['cart'][$key]);
        }
    }

    // Cập nhật lại chỉ mục mảng
    $_SESSION['cart'] = array_values($_SESSION['cart']);
}

// Tính toán tổng giá trị giỏ hàng
$total_price = 0;
foreach ($_SESSION['cart'] as $item) {
    $total_price += $item['product_price'] * $item['quantity'];
}
